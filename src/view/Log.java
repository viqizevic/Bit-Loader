package view;

/**
 * The Class Log.
 */
public class Log {
	
	/**
	 * P.
	 *
	 * @param message the message
	 */
	public static void p(String message) {
		System.out.println(message);
	}
	
	/**
	 * E.
	 *
	 * @param message the message
	 */
	public static void e(String message) {
		System.err.println(message);
	}

}
